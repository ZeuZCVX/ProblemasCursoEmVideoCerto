#AREA DE UMA PAREDE E QUANTIDADE DE TINTA QUE ULTILIZA


altura = float(input('Digite a altura da parede: '))
largura = float(input('Digite a largura da parede: '))

a = altura * largura
print('A area quadrada da sua parede e {:.1f}'.format(a))
ti = a / 2
print('Quantidade de tina utilizada e {:.1f}'.format(ti))